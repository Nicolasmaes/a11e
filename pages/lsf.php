                <h2 class="main__lsf__paragraphe__titre">Applications utilisant la langue des signes</h2>

                <p class="main__lsf__paragraphe__text">
                    Pour l'instant, il n'existe pas ou peut d'application pour sourds utilisant la langue des signes.
                    Il existe en revanche des  applications de sous-titrage (comme rogervoice ou ava) ou des applications de traîtement de son (a destination des mal-entendants).

                    <br><br>Cela est sans doute dû au fait que retranscrire une conversationen LSF via un algorithme serait techniquement plus compliquer que  généré des sous-titres :  en effet, il faudrait pour cela analyser des information autre que les seuls mots, et la traduction passerait non-pas par des caractères mais par exemple par une animation complexe, impliquant des mouvement de main mais aussi de tout le corps, incluant des expressions faciales.
                </p> 
