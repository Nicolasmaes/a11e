                <h2 class="main__lsf__paragraphe__titre">Grammaire de la LSF</h2>

                <p class="main__lsf__paragraphe__text">
                    La langue des signes française permet d'exprimer plusieurs idées simultanément,ce qui la différencie de la grammaire française classique.<br><br>

                    -L'expressions du visage peut indiquer le sens de la phrase (par exemple pour dire qu'il s'agit d'une question).<br>
                    -Il n’existe pas de conjugaison en LSF, il suffit au signeur de situer l’action sur la ligne du temps (perpendiculaire à lui:  derrière son épaule le passé, au niveau de son corps le présent et devant lui le futur).<br>
                    -Des gestes montres le rôle ou la forme du référent: Pour faire  référence à un homme, le locuteur utilisera son index vers le  haut,mais pour parler d’une voiture, il utilisera la main à plat. Les verbes de mouvement peuvent également être  signés pour indiquer  la direction :
                    « La voiture tourne à gauche »  sera le classificateur de voiture tournant à gauche.<br>
                    -Autre exemple,« Un homme monte l’escalier » se fera par deux doigts imitant les jambes montant un escalier, dans un mouvement pouvant être différent selon qu’il s’agit d’un escalier classique ou en colimaçon.<br>
                    -Pour parler de personnes absentes le signeur définit sa place et celle des autres dans l’espace, à la  manière d’une mise en scène théâtrale, il est ainsi plus facile de  comprendre et de suivre visuellement de qui il s’agit et quels sont les rapports entre les personnages.<br><br>
                    
                    En bref, la LSF est très riche et permet une communication très complète, qui viens en quelque sorte combler par le visuel le manque à gagner auditif.
                </p>
