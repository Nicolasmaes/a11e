                    Même si on est très loin d'une pratique systématique, on a tous déjà vu des vidéos, notamment de discours officiels,avec incrustation dans un coin de l'écran d'une personne traduisant les dialogues en temps réel. 
                    <br><br>Comme vu précédemment, cela permet a priori de retranscrire plus fidèlement le discours que  par les sous-titres.
                    En effet, et notamment dans le cadre d'une traduction instantanée, dure d'imaginer retranscrire par des  sous-titres, les subtilités de la langue parlée.
                    Tout simplement car l'écrit n'est pas fait pour cela : le descriptif complet des subtilités du language oral, incluant les intonations par exemple, serait trop long pour du temps réel.
